# 字符串
x = str("Hello World")
print(x)
print("字符串")

# 整数
x = int(20)
print(x)
print("整数")

# 浮点数
x = float(20.5)
print(x)
print("浮点数")

# 复数
x = complex(1j)
print(x)
print("复数")

# 列表
x = list(("apple", "banana", "cherry"))
print(x)
print("列表")

# 元组
x = tuple(("apple", "banana", "cherry"))
print(x)
print("元组")

# 范围
x = range(6)
print(x)
print("范围")

# 字典
x = dict(name="John", age=36)
print(x)
print("字典")

# 集合
x = {"apple", "banana", "cherry"}
print(x)
print("集合")

# 冻结集合
x = frozenset(("apple", "banana", "cherry"))
print(x)
print("冻结集合")

# 布尔值
x = bool(5)
print(x)
print("布尔值")

# 字节
x = bytes(5)
print(x)
print("字节")

# 字节数组
x = bytearray(5)
print(x)
print("字节数组")

# 内存视图
x = memoryview(bytes(5))
print(x)
print("内存视图")
